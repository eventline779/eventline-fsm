#!/usr/bin/env node
// Generiert src/lib/build-info.ts mit aktuellen Code-Stats + Git-Info.
// Wird von npm "predev" und "prebuild" aufgerufen — das resultierende
// File wird vom Einstellungen-Fun-Fact-Widget importiert.

import { execSync } from "node:child_process";
import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { join, sep } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = fileURLToPath(new URL("..", import.meta.url));

function safeGit(cmd) {
  try {
    return execSync(cmd, { stdio: ["pipe", "pipe", "ignore"], cwd: ROOT }).toString().trim();
  } catch {
    return null;
  }
}

// Rekursiv alle Code-Dateien einsammeln. Native fs.readdirSync mit recursive
// braucht Node 20+ — das nutzen wir hier (Vercel-Build-Env hat das).
function* walk(dir, exts) {
  let entries;
  try {
    entries = readdirSync(dir, { withFileTypes: true });
  } catch {
    return;
  }
  for (const entry of entries) {
    if (entry.name.startsWith(".") || entry.name === "node_modules" || entry.name === "dist" || entry.name === ".next") continue;
    const full = join(dir, entry.name);
    if (entry.isDirectory()) {
      yield* walk(full, exts);
    } else if (exts.some((e) => entry.name.endsWith(e))) {
      yield full;
    }
  }
}

const codeFiles = [
  ...walk(join(ROOT, "src"), [".ts", ".tsx", ".css"]),
  ...walk(join(ROOT, "supabase", "migrations"), [".sql"]),
];

let totalLines = 0;
let totalWords = 0;
for (const file of codeFiles) {
  const content = readFileSync(file, "utf-8");
  totalLines += content.split("\n").length;
  totalWords += content.split(/\s+/).filter(Boolean).length;
}

const sha = safeGit("git rev-parse --short HEAD") ?? "dev";
const commitCount = parseInt(safeGit("git rev-list --count HEAD") ?? "0", 10);
const branch = safeGit("git rev-parse --abbrev-ref HEAD") ?? "dev";

const buildInfo = {
  version: `0.${commitCount}`,
  sha,
  branch,
  buildAt: new Date().toISOString(),
  files: codeFiles.length,
  lines: totalLines,
  words: totalWords,
};

const out = `// Auto-generated by scripts/generate-build-info.mjs — DO NOT EDIT.
// Wird bei jedem 'npm run dev' und 'npm run build' regeneriert.
export const BUILD_INFO = ${JSON.stringify(buildInfo, null, 2)} as const;
`;

const outPath = join(ROOT, "src", "lib", "build-info.ts");
writeFileSync(outPath, out);

console.log(`[build-info] v${buildInfo.version} (${buildInfo.sha}) · ${buildInfo.files} Dateien · ${buildInfo.lines.toLocaleString()} Zeilen · ${buildInfo.words.toLocaleString()} Wörter${sep}`);
